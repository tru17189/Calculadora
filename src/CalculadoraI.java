public class CalculadoraI {
    public int suma(int a, int b, int c){   
    c = a + b; 
    return c; 
    }
    public int resta(int a, int b, int c){   
    c = a - b; 
    return c;
    }
    public int multiplicar(int a, int b, int c){   
    c = a * b; 
    return c;
    }
    public int dividir(int a, int b, int c){   
    c = a / b; 
    return c;
    }
    
}
